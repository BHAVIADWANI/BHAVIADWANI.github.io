/** Generate a storage key from instrument name for Supabase storage */
export function getInstrumentStorageKey(name: string): string {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/-+/g, '-').replace(/^-|-$/g, '');
}

